"""In-app help window.

Loads the pre-built data/help.html (generated by scripts/build_help.py) into
a WebKit view.  Falls back to a formatted TextBuffer view when WebKit is not
available.
"""
from __future__ import annotations

import logging
import re
from pathlib import Path

import gi
gi.require_version("Gtk", "4.0")
gi.require_version("Adw", "1")
gi.require_version("Gdk", "4.0")
from gi.repository import Adw, Gdk, Gtk

from .auth_dialog import WebKit, WEBKIT_AVAILABLE

log = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# File location helpers
# ---------------------------------------------------------------------------

def _find_help_html() -> Path | None:
    """Locate the pre-built help.html file, rebuilding it in dev if stale."""
    candidates = [
        Path(__file__).resolve().parent.parent.parent.parent / "data" / "help.html",
        Path("/app/share/doc/com.seravault.cloudsync/help.html"),
    ]
    for p in candidates:
        if p.is_file():
            # In the source tree, rebuild if user-guide.md is newer than help.html
            src_md = p.parent / "docs" / "user-guide.md"
            if src_md.is_file() and src_md.stat().st_mtime > p.stat().st_mtime:
                _rebuild_help(p.parent.parent)
            return p
    # Not found — try to build it now (dev source tree only)
    src_root = Path(__file__).resolve().parent.parent.parent.parent
    if (src_root / "scripts" / "build_help.py").is_file():
        _rebuild_help(src_root)
        out = src_root / "data" / "help.html"
        if out.is_file():
            return out
    return None


def _rebuild_help(src_root: Path) -> None:
    """Run scripts/build_help.py from the given source root."""
    import subprocess, sys
    script = src_root / "scripts" / "build_help.py"
    try:
        subprocess.run([sys.executable, str(script)], check=True, cwd=src_root,
                       capture_output=True)
        log.debug("Rebuilt data/help.html")
    except Exception as exc:
        log.warning("Could not rebuild help.html: %s", exc)


def _find_readme() -> Path | None:
    """Locate user-guide.md (used by the plain-text fallback)."""
    candidates = [
        Path(__file__).resolve().parent.parent.parent.parent / "data" / "docs" / "user-guide.md",
        Path("/app/share/doc/com.seravault.cloudsync/user-guide.md"),
    ]
    for p in candidates:
        if p.is_file():
            return p
    return None



