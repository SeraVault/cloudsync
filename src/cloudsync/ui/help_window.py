class HelpWindow(Adw.Window):
    """A non-modal window that renders README.md as HTML via WebKit."""

    def __init__(self, parent: Gtk.Window) -> None:
        super().__init__(
            title="CloudSync Help",
            transient_for=parent,
            default_width=880,
            default_height=720,
            resizable=True,
        )

        toolbar_view = Adw.ToolbarView()

        # --- Header bar with search toggle ---
        header = Adw.HeaderBar()
        search_btn = Gtk.ToggleButton(
            icon_name="system-search-symbolic",
            tooltip_text="Search (Ctrl+F)",
        )
        header.pack_end(search_btn)
        toolbar_view.add_top_bar(header)

        # Keyboard shortcut: Ctrl+F toggles search
        ctrl = Gtk.ShortcutController(scope=Gtk.ShortcutScope.LOCAL)
        ctrl.add_shortcut(Gtk.Shortcut(
            trigger=Gtk.KeyvalTrigger.new(ord("f"), Gdk.ModifierType.CONTROL_MASK),
            action=Gtk.CallbackAction.new(lambda *_: search_btn.set_active(not search_btn.get_active()) or True),
        ))
        self.add_controller(ctrl)

        # --- Locate content ---
        # WebKit path: load the pre-built help.html via file:// URI.
        # Text fallback: load README.md and render with _fill_text_buffer.
        readme = _find_readme()
        if readme:
            try:
                content = readme.read_text(encoding="utf-8")
            except OSError as exc:
                log.warning("Could not read README.md: %s", exc)
                content = "# Help\n\nCould not load README.md."
        else:
            content = "# Help\n\nREADME.md not found."

        help_html = _find_help_html()

        _use_webkit = False
        if WEBKIT_AVAILABLE and help_html:
            try:
                web = WebKit.WebView()
                web.set_hexpand(True)
                web.set_vexpand(True)
                web.load_uri(help_html.as_uri())
                _use_webkit = True
            except Exception as exc:
                log.warning("WebKit failed, falling back to text view: %s", exc)

        if _use_webkit:
            find_controller = web.get_find_controller()
            _FIND_OPTS = (
                WebKit.FindOptions.CASE_INSENSITIVE
                | WebKit.FindOptions.WRAP_AROUND
            )

            # --- Search bar (WebKit path) ---
            search_bar = Gtk.SearchBar(show_close_button=False)
            search_entry = Gtk.SearchEntry(
                placeholder_text="Search…",
                hexpand=True,
                max_width_chars=40,
            )
            prev_btn = Gtk.Button(icon_name="go-up-symbolic", tooltip_text="Previous")
            prev_btn.add_css_class("flat")
            next_btn = Gtk.Button(icon_name="go-down-symbolic", tooltip_text="Next")
            next_btn.add_css_class("flat")

            search_box = Gtk.Box(spacing=4)
            search_box.append(search_entry)
            search_box.append(prev_btn)
            search_box.append(next_btn)
            search_bar.set_child(search_box)

            def _do_search(*_):
                text = search_entry.get_text()
                if text:
                    find_controller.search(text, _FIND_OPTS, 1000)
                else:
                    find_controller.search_finish()

            def _on_search_bar_toggled(btn):
                active = btn.get_active()
                search_bar.set_search_mode(active)
                if active:
                    search_entry.grab_focus()
                else:
                    find_controller.search_finish()
                    search_entry.set_text("")

            search_entry.connect("search-changed", _do_search)
            prev_btn.connect("clicked", lambda _: find_controller.search_previous())
            next_btn.connect("clicked", lambda _: find_controller.search_next())
            search_entry.connect("activate", lambda _: find_controller.search_next())
            search_btn.connect("toggled", _on_search_bar_toggled)

            # Wrap WebView + search bar in a vertical box
            content_box = Gtk.Box(orientation=Gtk.Orientation.VERTICAL)
            content_box.append(search_bar)
            content_box.append(web)
            toolbar_view.set_content(content_box)

        else:
            # --- Fallback: formatted TextBuffer view ---
            buf = Gtk.TextBuffer()
            _fill_text_buffer(buf, content)
            highlight_tag = buf.create_tag("search-highlight", background="yellow", foreground="black")

            tv = Gtk.TextView(
                buffer=buf,
                editable=False,
                wrap_mode=Gtk.WrapMode.WORD,
                top_margin=16,
                left_margin=20,
                right_margin=20,
                bottom_margin=16,
            )
            scroll = Gtk.ScrolledWindow(
                hscrollbar_policy=Gtk.PolicyType.NEVER,
                vexpand=True,
            )
            scroll.set_child(tv)

            # Search bar (plain-text fallback)
            search_bar = Gtk.SearchBar(show_close_button=False)
            search_entry = Gtk.SearchEntry(
                placeholder_text="Search…",
                hexpand=True,
                max_width_chars=40,
            )
            prev_btn = Gtk.Button(icon_name="go-up-symbolic", tooltip_text="Previous")
            prev_btn.add_css_class("flat")
            next_btn = Gtk.Button(icon_name="go-down-symbolic", tooltip_text="Next")
            next_btn.add_css_class("flat")

            search_box = Gtk.Box(spacing=4)
            search_box.append(search_entry)
            search_box.append(prev_btn)
            search_box.append(next_btn)
            search_bar.set_child(search_box)

            self._plain_matches: list = []
            self._plain_match_idx: int = -1

            def _plain_search(*_):
                buf.remove_tag(highlight_tag, buf.get_start_iter(), buf.get_end_iter())
                self._plain_matches = []
                self._plain_match_idx = -1
                text = search_entry.get_text().lower()
                if not text:
                    return
                start = buf.get_start_iter()
                while True:
                    found = start.forward_search(
                        text, Gtk.TextSearchFlags.CASE_INSENSITIVE, None
                    )
                    if not found:
                        break
                    match_start, match_end = found
                    buf.apply_tag(highlight_tag, match_start, match_end)
                    self._plain_matches.append(buf.create_mark(None, match_start, True))
                    start = match_end
                if self._plain_matches:
                    _plain_jump(0)

            def _plain_jump(idx: int):
                if not self._plain_matches:
                    return
                self._plain_match_idx = idx % len(self._plain_matches)
                mark = self._plain_matches[self._plain_match_idx]
                it = buf.get_iter_at_mark(mark)
                tv.scroll_to_iter(it, 0.1, True, 0.0, 0.3)

            def _on_search_bar_toggled(btn):
                active = btn.get_active()
                search_bar.set_search_mode(active)
                if active:
                    search_entry.grab_focus()
                else:
                    buf.remove_tag(highlight_tag, buf.get_start_iter(), buf.get_end_iter())
                    search_entry.set_text("")

            search_entry.connect("search-changed", _plain_search)
            prev_btn.connect("clicked", lambda _: _plain_jump(self._plain_match_idx - 1))
            next_btn.connect("clicked", lambda _: _plain_jump(self._plain_match_idx + 1))
            search_entry.connect("activate", lambda _: _plain_jump(self._plain_match_idx + 1))
            search_btn.connect("toggled", _on_search_bar_toggled)

            content_box = Gtk.Box(orientation=Gtk.Orientation.VERTICAL)
            content_box.append(search_bar)
            content_box.append(scroll)
            toolbar_view.set_content(content_box)

        self.set_content(toolbar_view)
