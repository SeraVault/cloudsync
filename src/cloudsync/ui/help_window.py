def _fill_text_buffer(buf: Gtk.TextBuffer, text: str) -> None:
    """Render markdown into a TextBuffer using formatting tags."""
    h1_tag  = buf.create_tag("h1",  weight=700, scale=1.6, pixels_below_lines=6, pixels_above_lines=8)
    h2_tag  = buf.create_tag("h2",  weight=700, scale=1.3, pixels_below_lines=4, pixels_above_lines=6)
    h3_tag  = buf.create_tag("h3",  weight=700, scale=1.1, pixels_below_lines=2, pixels_above_lines=4)
    code_tag  = buf.create_tag("code_inline", family="monospace", scale=0.88)
    block_tag = buf.create_tag("code_block",  family="monospace", scale=0.86,
                               left_margin=24, right_margin=24,
                               pixels_above_lines=6, pixels_below_lines=6)
    bold_tag  = buf.create_tag("bold",  weight=700)
    dim_tag   = buf.create_tag("hr_dim", foreground="#999999")
    table_tag = buf.create_tag("table", family="monospace", scale=0.88)

    def ins(t: str, *tags) -> None:
        it = buf.get_end_iter()
        active = [tg for tg in tags if tg is not None]
        if active:
            buf.insert_with_tags(it, t, *active)
        else:
            buf.insert(it, t)

    def ins_inline(t: str, extra: "Gtk.TextTag | None" = None) -> None:
        for part in re.split(r"(`[^`]+`)", t):
            if part.startswith("`") and part.endswith("`") and len(part) > 1:
                ins(part[1:-1], code_tag, extra)
            else:
                for seg in re.split(r"(\*\*[^*]+\*\*)", part):
                    m = re.match(r"\*\*([^*]+)\*\*", seg)
                    if m:
                        ins(m.group(1), bold_tag, extra)
                    else:
                        clean = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", seg)
                        ins(clean, extra)

    lines = text.split("\n")
    i = 0
    while i < len(lines):
        line = lines[i]

        # Fenced code block
        if line.startswith("```"):
            i += 1
            code_lines: list[str] = []
            while i < len(lines) and not lines[i].startswith("```"):
                code_lines.append(lines[i])
                i += 1
            ins("\n".join(code_lines) + "\n", block_tag)
            i += 1
            continue

        # HR
        if re.fullmatch(r"-{3,}", line.strip()):
            ins("─" * 64 + "\n", dim_tag)
            i += 1
            continue

        # Heading
        m = re.match(r"(#{1,3}) (.*)", line)
        if m:
            tag = [h1_tag, h2_tag, h3_tag][len(m.group(1)) - 1]
            ins(m.group(2) + "\n", tag)
            i += 1
            continue

        # Table separator row — skip
        if re.match(r"^\s*\|[-| :]+\|\s*$", line):
            i += 1
            continue

        # Table data row
        if "|" in line and line.strip().startswith("|"):
            cells = [c.strip() for c in line.strip("|").split("|")]
            ins("  " + "   │   ".join(cells) + "\n", table_tag)
            i += 1
            continue

        # Bullet list
        m = re.match(r"^[-*] (.*)", line)
        if m:
            ins("  • ", bold_tag)
            ins_inline(m.group(1))
            ins("\n")
            i += 1
            continue

        # Blank line
        if line.strip() == "":
            ins("\n")
            i += 1
            continue

        # Paragraph
        ins_inline(line)
        ins("\n")
        i += 1


